<ol type="1">
    <li><h5>Stopa mlčení</h5>
        Otázal-li se kdo Indiána, co jest mlčení, odvětil:„Je to velké
        tajemství a mlčení je jeho hlasem.“ A otázal-li se kdo, jaké je ovoce mlčení, odpověděl:“Pravá
        statečnost, vytrvalost, trpělivost, důstojnost a uctivost.„Chce-li se někdo státi woodcrafterem a neumí
        mlčet, nesmí nejméně šest hodin za sebou promluviti jediného slova v kruhu svých druhů.např. při
        výpravě, akci kmene.
    </li>
    <li><h5>Stopa laskavosti</h5>
        Svět trpí, protože mu chybí láska. Woodcrafter je bratrem každému
        člověku dobré vůle. Kdo nedovede být laskavý, musí se o to pokusiti po dobu 12 hodin. Udržuj si dobrou
        náladu, nevyslov žádné hněvivé slovo a každému odpověz s úsměvem.
    </li>
    <li><h5>Stopa poslušnosti</h5>
        Poslušnost je podmínkou společného života. Je znakem síly,
        ukázněnosti a smyslu pro pořádek. Jeden týden poslouchej rodiče, učitele, trenéry, ale i sourozence nebo
        druha.
    </li>
    <li><h5>Stopa myslící ruky</h5>
        Je dnes mnoho lidí, kteří mají hlavy nacpané vědomostmi, ale
        nedovedou si ořezat pořádně tužku nebo vyrobit si nějaký užitečný předmět vlastníma rukama. Woodcrafter
        musí prokázat, že má „myslící ruku“, že dovede udělat něco praktického nebo umělecky krásného. Vyrob
        užitečný předmět pro kmen.
    </li>
    <li><h5>Stopa dobrého táborníka</h5>
        Naše cesta vede lesem. Umět žít v přírodě tak, aby se tam
        choval jako v kostele, ale přece se cítil jako doma, je jedním z hlavních požadavků. Stopu dobrého
        táborníka našel ten, kdo alespoň 3 noci po sobě venku spal pod širákem nebo přístřeškem.
    </li>
    <li><h5>Stopa svědomitosti</h5>
        Na svědomitosti spočívá celý úspěch woodcrafterské výchovy.
        Svědomitost je důkazem charekteru a cti. Kdo za 20 minut přinese dva stejné odkorněné vrbové proutky,
        tlusté jako tužka a dlouhé 30 cm prošel stopou svědomitosti.
    </li>
    <li><h5>Stopa odříkání</h5>
        Pro člověka je nutné se naučit ovládat svoji vůli. Tím převychovává
        sebe sama tj. „smyslem stoupání na horu“. Proto se vzdej poctivě na 1 týden nějaké hmotné příjemné věci,
        která není nutná např.: sladkostí, sledování televize apod.
    </li>
    <li><h5>Stopa lásky k přírodě</h5>
        Namaluj chráněnou rostlinu a vystav ji v klubovně. Můžeš použít netradiční techniku nebo materiál.
    </li>
    <li><h5>Stopa osamělého táborníka</h5>
        Vezmi pokrývky, sekeru, sirky atd. připrav si dřevo
        (můžeš mít pomocníka)na strážní ohýnek a po západu slunce bdi u malého ohníčku sám od soumraku do
        rozbřesku bez jídla, čtení, spaní, mluvení, návštěv v tábořišti kmene jako osamělá hlídka.Navracej se na
        lůžko až po východu slunce.
    </li>
    <li><h5>Stopa otužilosti</h5>
        Jsme většinou nuceni žíti v nepříliš zdravém městském prostředí,
        místnostmi s malou vlhkostí vzduchu a podobně. Dovedeme si však pomoci. Všechny své volné chvíle
        věnujeme rekreaci ve volné přírodě.Otužuj svoje zdravé tělo studenou vodou 7 dní po sobě. Získáváš tím
        odolnost proti nemocím,jasnou mysl a poznání radostné stopy zasvěcovací stezky. Kmenový zvyk na letním
        táboře říká, potop se po bradu do tůňky a nevynoř se dřív než bojovníci napočítají do třiceti (30sec).
    </li>
    <li><h5>Stopa krásy</h5>
        Po 1 týden v týpí nebo 1 měsíc doma udržuj pořádek svých věcí tak, aby
        ti nikdo nemohl nic vytknout, a vždy vše sám najdeš.
    </li>
</ol>
